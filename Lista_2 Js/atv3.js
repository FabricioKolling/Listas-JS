//3) Programar a conversão de uma temperatura digitada pelo usuário em graus Celsius para Fahrenheit. Mostrar o resultado na tela. 

celsius = Number(prompt('Digite quantos graus em Celsius você quer transformar em Fahrenheit: '))

fahrenheit = (celsius * 1.8 + 32)

alert('Aqui está sua temperatura em Fahrenheit: '+ fahrenheit)